
package who.cares;


public class TacoShop {
    String name;
    String logo;
    
    public TacoShop (String name, String logo) {
        this.name = name;
        this.logo = logo;
    }
    
    public String getName() {
        return this.name;
    }
    
    public String getLogo() {
        return this.logo;
    }
    
}
